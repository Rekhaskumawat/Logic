void DisplayFactors(int iNo)
{
    if(iNo < 0)
    {
        iNo = -iNo ;
    }

    int iCnt = 0;
    for(iCnt = 1; iCnt < iNo ; iCnt++)
    {
        if((iNo % iCnt )== 0)
        {
           printf("%d\t", iCnt);
        } 

    }
   
    
}//End of DisplayFactors function
//Time complexity : O(N)

///////////////////////////////////////////////////////////////////////////////
//
//Entery point function
//
///////////////////////////////////////////////////////////////////////////////

int main()
{
   int iValue = 0;

   printf("Enter  Number:");
   scanf("%d",&iValue);
   DisplayFactors(iValue);

    return 0;
}//end of main function

