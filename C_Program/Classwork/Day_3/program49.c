//
//Entery point function
//
///////////////////////////////////////////////////////////////////////////////
int main()
{
   int iValue = 0;

   printf("Enter  Number:");
   scanf("%d",&iValue);
   DisplayNonFactors(iValue);

    return 0;
}//end of main function

