void DisplayNonFactors(int iNo)
{
    if(iNo < 0)
    {
        iNo = -iNo ;
    }

    int iCnt = 0;
    for(iCnt = 1; iCnt <= iNo ; iCnt++)
    {
        if((iNo % iCnt )!= 0)
        {
           printf("%d\t", iCnt);
        } 

    }
   
    
}// end of DisplyNonFactors function
//Time complexity : O(N)

