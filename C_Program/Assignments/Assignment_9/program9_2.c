{
    int iValue = 0 ;
    int iRet = 0;

    printf("Enter number of USD :");
    scanf("%d",&iValue);

    iRet = DollerToINR(iValue);

    printf("Value in INR is %d ", iRet);

    return 0 ;
}// end of main function

