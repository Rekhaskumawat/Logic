void Display(int iNo)
{
    int iCnt = 0 ;

    if(iNo < 0)
    {
        iNo = -iNo;
    }
    for(iCnt = 1 ; iCnt <= iNo ; iCnt++)
    {
        printf("*\t");
    }
    for(iCnt = 1 ; iCnt <= iNo ; iCnt++)
    {
        printf("#\t");
    }
}// end of Display function

///////////////////////////////////////////////////////////////////////////////
//
//Entery point function
//
///////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0 ;

    printf("Enter the number:");
    scanf("%d",&iValue);

    Display(iValue);
    return 0 ;

}// end of main function

