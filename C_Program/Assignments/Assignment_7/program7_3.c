///////////////////////////////////////////////////////////////////////////////
//
//  Function Name : Display
//  Description :   to print the number line of that number
//  Input :         Integer
//  output :        void
//  Author :        Rekha Shankarlal Kumawat
//  Date :          19/10/2025
