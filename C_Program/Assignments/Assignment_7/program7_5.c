void MultipleDisplay(int iNo)
{
    int iCnt = 0;

    for(iCnt = 1 ; iCnt <= 5 ; iCnt++)
    {
        int iMult= 0;
        
        iMult= iNo* iCnt ;
        printf("%d\t",iMult);
        
        
    }
}// End of MultipleDisplay function

///////////////////////////////////////////////////////////////////////////////
//
//Entery point function
//
///////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    printf("Enter number: ");
    scanf("%d",&iValue);

    MultipleDisplay(iValue);
    return 0 ;
}// end of main function

