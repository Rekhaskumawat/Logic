///////////////////////////////////////////////////////////////////////////////
//
//  Function Name : MultipleDisplay
//  Description :   to print first five multiple of number
//  Input :         Integer
//  output :        void
//  Author :        Rekha Shankarlal Kumawat
//  Date :          20/10/2025
//
