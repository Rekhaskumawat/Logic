void Display(int iNo)
{
    int iCnt = 0;

    for(iCnt = 1 ; iCnt < iNo ; iCnt++)
    {
        if(iCnt % 2 == 1)
        {
            printf("%d\t",iCnt);
        }
        
    }
}// End of Display function

///////////////////////////////////////////////////////////////////////////////
//
//Entery point function
//
///////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    printf("Enter number: ");
    scanf("%d",&iValue);

    Display(iValue);
    return 0 ;
}// end of main function

