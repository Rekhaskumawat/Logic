int Sum_Natural_Number(int iNo)
{
    int iCnt = 0;
    int iSum = 0 ;
    
    for(iCnt = 1 ; iCnt <= iNo ; iCnt++ )
    {
        
            iSum = iSum+iCnt ;
        
        
    }

    return iSum ;
    
}// time complexity is O(N)

//end of Sum_Natural_Number function

///////////////////////////////////////////////////////////////////////////////
//
//Entery point function
