///////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;
    int iRet = 0;

    printf("enter the number :");
    scanf("%d ",&iValue);

    iRet = Sum_Natural_Number(iValue);
    printf("Sum : %d", iRet);

    return 0 ;
}// End of main function

