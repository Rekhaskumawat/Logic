void Print_Even_Number(int iNo)
{
    int iCnt = 0;
    
    for(iCnt =1 ; iCnt <= iNo ; iCnt++ )
    {
        if(iCnt%2 == 0)
        {
            printf("%d\t ", iCnt);
        }
        
    }
    
}// time complexity is O(N)

//end of Print_Even_Number function

///////////////////////////////////////////////////////////////////////////////
//
//Entery point function
//
///////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;
    

    printf("enter the number :");
    scanf("%d ",&iValue);

    Print_Even_Number(iValue);
    

    return 0 ;
}// End of main function

