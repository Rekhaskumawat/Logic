///////////////////////////////////////////////////////////////////////////////
//
//  Function Name : RangeDisplay
//  Description :   to display range of number
//  Input :         Integer
//  output :        void
//  Author :        Rekha Shankarlal Kumawat
//  Date :          21/10/2025
//
