void Print_Factors(int iNo)
{
    int iCnt = 0;
    for(iCnt =1 ; iCnt <= (iNo/2) ; iCnt++ )
    {
        if(iNo%iCnt == 0)
        {
            printf("%d \t",iCnt);
        }
    }
}// time complexity is O(N/2)

//end of Print_Factors function

