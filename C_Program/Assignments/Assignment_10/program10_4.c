///////////////////////////////////////////////////////////////////////////////
//
//  Function Name : FhtoCs
//  Description :   to conveert temperature from fahrenheit to celsius
//  Input :         float
//  output :        double
//  Author :        Rekha Shankarlal Kumawat
//  Date :          21/10/2025
