double FhtoCs(float fTemp)
{
    double dCelsius = 0.0 ;

    dCelsius = (fTemp- 32.0)*(5.0/9.0);

    return dCelsius ;

}//end of FhtoCs function

///////////////////////////////////////////////////////////////////////////////
//
//Entery point function
//
///////////////////////////////////////////////////////////////////////////////

int main()
{
    float fValue = 0.0 ;
    double dRet = 0.0 ;

    printf("Enter temperature in fahrenheit :");
    scanf("%f ",&fValue);

    dRet = FhtoCs(fValue);
    printf("temprature in Celsius  %lf ", dRet);

    return 0;
}// end of main function

