int KMToMeter(int iNo)
{
    int iConverter = 0 ;

    iConverter = iNo*1000 ;

    return iConverter ;
}//end of KMToMeter function

///////////////////////////////////////////////////////////////////////////////
//
//Entery point function
//
///////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0 ;
    int iRet = 0 ;

    printf("Enter distance :");
    scanf("%d",&iValue);

    iRet = KMToMeter(iValue);
    printf("distance in meter %d ", iRet);

    return 0 ;
}// end of main function

