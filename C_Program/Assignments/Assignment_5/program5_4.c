void CheckNumber(int iNo)
{
    if(iNo < 0 )
    {
        printf("%d is negative number",iNo);
    }
    else if(iNo > 0 )
    {
        printf("%d is positive number",iNo);
    }
    else if(iNo == 0 )
    {
        printf("%d is Zero",iNo);
    }
}// end of CheckNumber

///////////////////////////////////////////////////////////////////////////////
//
//Entery point function
//
///////////////////////////////////////////////////////////////////////////////

int main()
{
    int ivalue = 0;
    printf("Enter Number :");
    scanf("%d",&ivalue);

    CheckNumber(ivalue);
    return 0;

}// end of main function

