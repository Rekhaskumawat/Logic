void CheckEvenOdd(int iNo)
{
    if((iNo % 2) == 0)
    {
        printf("%d is Even number",iNo);
    }
    else
    {
        printf("%d is odd number",iNo);
    }
}// End of CheckEvenOdd function

///////////////////////////////////////////////////////////////////////////////
//
//Entery point function
//
///////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    printf("Enter the number :");
    scanf("%d",&iValue);

    CheckEvenOdd(iValue);
 
    return 0 ;
}// End of main function

