//
//Entery point function
//
///////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    printf("Enter the year :");
    scanf("%d",&iValue);

    CheckLeapYear(iValue);

    return 0;
}// end of main function

