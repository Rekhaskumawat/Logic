void CheckLeapYear(int iNo)
{
    if((iNo%4 ) == 0 || (iNo%400) == 0)
    {
        printf("%d is a leap year",iNo);
    }
    else
    {
        printf("%d is not a leap year", iNo);
    }
}// end of CheckLeapYear function

