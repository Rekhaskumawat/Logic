int FindMax( int iNo1 ,
             int iNo2
            )
{
    if(iNo1 > iNo2)
    {
        return iNo1 ;
    }
    else
    {
        return iNo2 ;
    }
}// End of FindMax function

///////////////////////////////////////////////////////////////////////////////
//
//Entery point function
//
///////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue1 = 0 , iValue2 = 0;
    int iRet = 0 ;

    printf("Enter two numbers :");
    scanf("%d%d",&iValue1,&iValue2);

    iRet = FindMax(iValue1 , iValue2);
    printf("Maximum number:%d", iRet);

    return 0;
}

