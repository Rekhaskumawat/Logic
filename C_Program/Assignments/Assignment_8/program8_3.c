int main()
{
    int iValue = 0 ;
    int iRet = 0 ;

    printf("Enter the number :");
    scanf("%d",&iValue);

    iRet = Factorial(iValue);
    printf("Factorial of number :%d",iRet);

    return 0 ;
}// end of main function

