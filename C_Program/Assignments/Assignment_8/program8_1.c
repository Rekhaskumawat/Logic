void Number(int iNo)
{
    if(iNo <= 50)
    {
        printf("Less");

    }
    else if(iNo> 50  && iNo <100)
    {
        printf("Medium");
    }
    else
    {
        printf("Large");
    }
}// end of number function 

///////////////////////////////////////////////////////////////////////////////
//
//Entery point function
//
///////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0 ;

    printf("Enter number:");

    scanf("%d",&iValue);

    Number(iValue);

    return 0;

}// end of main function

