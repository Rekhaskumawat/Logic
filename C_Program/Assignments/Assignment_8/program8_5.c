void TableRev(int iNo)
{
    int iCnt = 0;
    for(iCnt = 10 ; iCnt >= 1 ; iCnt--)
    {
        printf("%d\t",iCnt*iNo);
    }
}// end of TableRev function

///////////////////////////////////////////////////////////////////////////////
//
//Entery point function
//
///////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    printf("Enter the number :");
    scanf("%d",&iValue);

    TableRev(iValue);
    return 0;
}// end of main function

