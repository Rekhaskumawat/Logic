///////////////////////////////////////////////////////////////////////////////
//
//  Function Name : TableRev
//  Description :   to print table in reverse 
//  Input :         Integer
//  output :        Void
//  Author :        Rekha Shankarlal Kumawat
//  Date :          20/10/2025
