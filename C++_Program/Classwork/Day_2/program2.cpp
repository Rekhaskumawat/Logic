bool CheckEvenOdd(int iNo)
{
 
    if((iNo % 2) == 0)
    {
        return true;
    }
    else
    {
        return false;
    }
}

///////////////////////////////////////////////////////////////////////////////
//
//Entery point function
//
///////////////////////////////////////////////////////////////////////////////
int main()
{
    int iValue = 0;
    bool bRet = false;

    cout<<"Enter Number:"<<"\n";
    cin>>iValue ;

    bRet = CheckEvenOdd(iValue);

    if(bRet == true)
    {
        cout<<iValue << " is Even Number"<<"\n";
    }
    else
    {
        cout<<iValue << " is Odd Number"<<"\n";
    }

    return 0; 
}

